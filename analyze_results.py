#!/usr/bin/env python3
"""
analyze_results.py — Turn raw real-eval output into the headline AgentRisk
numbers and the binary-vs-RI re-ranking, and emit a LaTeX table fragment.

Reads experiments/results/real_eval_results.json (produced by run_real_eval.py)
and prints, per model:
  * binary Exposure Leak Rate (ELR) and its rank,
  * AgentRisk Risk Index (RI) and its rank,
  * per-severity leak rates,
and flags any "Low-Binary Trap": a model whose binary rank is better (lower)
than its RI rank, i.e. it leaks fewer items but more severe ones.
"""
from __future__ import annotations

import json
from pathlib import Path

HERE = Path(__file__).resolve().parent
RES = HERE / "results" / "real_eval_results.json"
OUT_TEX = HERE / "results" / "real_eval_table.tex"


def ranks(values: dict[str, float]) -> dict[str, int]:
    # rank 1 = lowest (best, since lower leakage is better)
    order = sorted(values, key=lambda k: values[k])
    return {k: i + 1 for i, k in enumerate(order)}


def main() -> int:
    run = json.loads(RES.read_text())
    models = run["models"]

    elr = {m: info["summary"]["ELR_binary"] for m, info in models.items()}
    ri = {m: info["summary"]["RI"] for m, info in models.items()}
    elr_rank = ranks(elr)
    ri_rank = ranks(ri)

    print(f"{'model':<24} {'ELR':>7} {'rank':>4} | {'RI':>7} {'rank':>4} | "
          f"{'L1':>5} {'L2':>5} {'L3':>5} {'L4':>5}")
    print("-" * 78)
    for m, info in models.items():
        s = info["summary"]
        ps = s["per_severity_leak_rate"]
        trap = " <-- Low-Binary Trap" if elr_rank[m] < ri_rank[m] else ""
        print(f"{m:<24} {elr[m]:>7.3f} {elr_rank[m]:>4} | "
              f"{ri[m]:>7.3f} {ri_rank[m]:>4} | "
              f"{ps['1']:>5.2f} {ps['2']:>5.2f} {ps['3']:>5.2f} {ps['4']:>5.2f}{trap}")

    # LaTeX fragment
    lines = [
        r"% auto-generated by analyze_results.py",
        r"\begin{tabular}{lcccccc}",
        r"\toprule",
        r"Model & ELR (binary) & RI & L1 & L2 & L3 & L4 \\",
        r"\midrule",
    ]
    for m, info in models.items():
        s = info["summary"]
        ps = s["per_severity_leak_rate"]
        name = m.split("/")[-1].replace("_", r"\_")
        lines.append(
            f"{name} & {elr[m]:.3f} & \\textbf{{{ri[m]:.3f}}} & "
            f"{ps['1']:.2f} & {ps['2']:.2f} & {ps['3']:.2f} & {ps['4']:.2f} \\\\"
        )
    lines += [r"\bottomrule", r"\end{tabular}"]
    OUT_TEX.write_text("\n".join(lines) + "\n")
    print(f"\nLaTeX table -> {OUT_TEX}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
